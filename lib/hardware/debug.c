#if defined(DEBUG)

#include "debug.h"

#include "stm32f4xx_hal.h"

void debug_init(void) {
    CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
    DWT->CYCCNT = 0;
    DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;
}

uint32_t debug_get_counter(void) { return DWT->CYCCNT; }

#endif
